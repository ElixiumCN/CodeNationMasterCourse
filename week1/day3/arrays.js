let coffeeOrder = [
    "Alex - Cortado",
    "Cortado",
    "Whatever's new"
    // ['something']
];

// overwrite array item with new value
coffeeOrder[1] = "Tea"

// javascript starts counting at zero.
// print selected array
console.log(coffeeOrder[2]);

// print entire array
console.log(coffeeOrder);

// how many items are in the array?
console.log(coffeeOrder.length);

// add a new item to the end of the array
coffeeOrder.push("Donna - espresso");

// removes the last item from an array
coffeeOrder.pop();

// there are lots of methods available to use in arrays

// hover over method name to read info about it.
coffeeOrder.splice(1, 1);





